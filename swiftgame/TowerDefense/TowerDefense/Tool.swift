public struct MapPoint {
	public var x: Int
	
	public var y: Int
	
	public init(x: Int, y: Int) {
		self.x = x
		self.y = y
	}
	
	func fixToMapPositon() -> CGPoint {
		let y = mapPosition.y + CGFloat(mapRowCount - 1 - self.x) * itemHeight + itemHeight * 0.5
		let x = mapPosition.x + CGFloat(self.y) * itemWidth + itemWidth * 0.5
//		print("x:\(self.x), y:\(self.y) -> CGPoint(\(x), \(y))")
		return CGPoint.init(x: x, y: y)
	}
}

extension CGPoint {
	
	func fixToMapPoint() -> MapPoint {
		let x = mapRowCount - 1 - Int(floor((self.y - mapPosition.y) / itemHeight))
		let y = Int(floor((self.x - mapPosition.x) / itemWidth))
		return MapPoint.init(x: x, y: y)
	}
	
	func fixToMapPositon() -> CGPoint {
		let indexPoint = self.fixToMapPoint()
		return indexPoint.fixToMapPositon()
	}
	
	
	func distance(to point: CGPoint) -> CGFloat {
		return sqrt(pow(self.x - point.x, 2) + pow(self.y - point.y, 2))
	}
	
	func angle(to point: CGPoint) -> CGFloat {
		let ox = point.x - self.x
		let oy = point.y - self.y

		let a = atan(abs(oy) / abs(ox))
		if ox > 0 {
			if oy > 0 {
				return CGFloat(Double.pi) * 1.5 + a
			} else {
				return CGFloat(Double.pi) * 1.5 - a
			}
		} else if ox < 0 {
			if oy > 0 {
				return CGFloat(Double.pi) * 0.5 - a
			} else {
				return CGFloat(Double.pi) * 0.5 + a
			}
		} else {
			if oy > 0 {
				return 0
			} else {
				return CGFloat(Double.pi)
			}
		}
	}
	
}

extension CGFloat {
	static var pi: CGFloat {
		get {
			return CGFloat(Double.pi)
		}
	}
	
	var toHuDu: CGFloat {
		get {
			return self * CGFloat.pi / 180
		}
	}
	
	var toJiaoDu: CGFloat {
		get {
			var a = Int(self * 180 / CGFloat.pi) % 360
			if a < 0 {
				a += 360
			}
			return CGFloat(a)
		}
	}
}

